package zadanie44;

public class zadanie44 {
    public static void main(String[] args) {
        int n = 10, wiersze,kolumny;


        for(wiersze=1; wiersze<=n; wiersze++){

            for(kolumny=1; kolumny<=n; kolumny++ ){
                System.out.print(wiersze*kolumny + "\t");
            }
            System.out.println();
        }




    }
}
